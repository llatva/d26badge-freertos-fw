/*
 * Menu icons – 24×24 monochrome bitmaps for the main menu
 *
 * Each icon is 24 pixels wide × 24 pixels tall, packed as row-major bits
 * (MSB first, 3 bytes per row).  '1' bits are foreground, '0' background.
 *
 * These are rendered with st7789_draw_bitmap() at 2× scale (48×48 px on screen).
 */

